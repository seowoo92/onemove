// 직전 문구와 겹치지 않게 랜덤 선택
export function pickTalk(coach, exceptIndex = -1) {
  const pool = COACH_TALK[coach] ?? COACH_TALK['다정']
  let idx = Math.floor(Math.random() * pool.length)
  if (pool.length > 1 && idx === exceptIndex) idx = (idx + 1) % pool.length
  return { text: pool[idx], index: idx }
}
